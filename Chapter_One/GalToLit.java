/* 
 * Convert Gallon To liter
 * 1 Gallon = 3.7854
*/


public class GalToLit {
    public static void main(String[] args) {
        double gal ;
        double lit ;
        gal = 10 ;
        lit = gal * 3.7854 ;
        System.out.println(gal + " gallon is " + lit + " liters" ) ;

    }
}
